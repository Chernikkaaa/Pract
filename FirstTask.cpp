int main() {
    // Виведення формули
    cout << "Формула: y = (7x - a) / (3 + log2(x)) - sqrt(|x + 3|)\n" << endl;

    double a, x, y;
    int mode;

    // Вибір режиму
    cout << "Оберіть режим:\n";
    cout << "1 - Ввести значення вручну\n";
    cout << "2 - Згенерувати випадкові значення\n";
    cout << "Ваш вибір: ";
    cin >> mode;

    if (mode == 1) {
        // Введення користувача
        cout << "Введіть значення параметра a: ";
        cin >> a;
        cout << "Введіть значення змінної x (x > 0): ";
        cin >> x;

        if (x <= 0) {
            cout << "Помилка: x повинен бути більше нуля для логарифма!" << endl;
            return 1;
        }
    }
    else if (mode == 2) {
        // Генерація випадкових значень
        srand(time(0));
        a = rand() % 10 + 1;           // a: від 1 до 10
        x = (rand() % 90 + 10) / 10.0; // x: від 1.0 до 10.0

        cout << "Згенеровані значення:\n";
        cout << "a = " << a << "\n";
        cout << "x = " << x << "\n";
    }
    else {
        cout << "Невірний вибір режиму!" << endl;
        return 1;
    }

    // Обчислення значення функції
    y = (7 * x - a) / (3 + log2(x)) - sqrt(fabs(x + 3));

    // Виведення результату
    cout << "Результат: y = " << y << endl;

    return 0;
}
